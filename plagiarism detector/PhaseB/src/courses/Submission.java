package courses;

/**
 * @author karan sharma
 * Submission class will have the functionality that will allow the students to submit their assignments
 */
public class Submission {

    /**
     * Every submission will have its number for a specific assignment.
     * For example: For Assignment 1 , each submission will be numbered from 1 to ...
     */
    private String SubmissionNo;


    /**
     * Every student will submit their assignments in a repository.
     * This field will record the web link ( ex. GitHub location ) of the assignment submitted by student
     */
    private String SubmissionLink;


}
